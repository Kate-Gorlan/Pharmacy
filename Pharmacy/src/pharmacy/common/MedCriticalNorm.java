package pharmacy.common;

public class MedCriticalNorm {

    private String medicament;
    private String medicamentType;
    private int medicamentQuantity;
    
    public String getMedicament() {
        return medicament;
    }
    public void setMedicament(String medicament) {
        this.medicament = medicament;
    }
    public String getMedicamentType() {
        return medicamentType;
    }
    public void setMedicamentType(String medicamentType) {
        this.medicamentType = medicamentType;
    }
    public int getMedicamentQuantity() {
        return medicamentQuantity;
    }
    public void setMedicamentQuantity(int medicamentQuantity) {
        this.medicamentQuantity = medicamentQuantity;
    }
    
}
